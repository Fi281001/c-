int main() {

            int a, A, B, C;

            printf("Nhap gia tri a:");

            scanf("%d", &a);

            A = a*a;

            B = a*a*a;

            C = a*a*a*a;

            printf("Binh phuong cua a: %d\n", A);

            printf("Lap phuong cua a: %d\n", B);

            printf("Gia tri a^4 la: %d", C);

            return 0;

}
